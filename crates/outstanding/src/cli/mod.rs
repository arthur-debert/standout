//! CLI integration for clap-based applications.
//!
//! This module provides integration with clap for building command-line applications
//! with Outstanding's rendering capabilities.
//!
//! Enable with the `clap` feature:
//!
//! ```toml
//! [dependencies]
//! outstanding = { version = "1.0", features = ["clap"] }
//! ```
//!
//! This module will contain:
//! - `App` - Main CLI handler (currently `Outstanding` in outstanding-clap)
//! - `AppBuilder` - Builder for configuring CLI apps
//! - `Handler` trait and `HandlerResult` types
//! - Command dispatch and hooks system
//! - Help rendering integration

